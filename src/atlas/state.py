# Enums
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


class TestLayer(str, Enum):
    """The 5 test generation layers."""
    UNIT = "UNIT"
    INTEGRATION = "INTEGRATION"
    FUNCTIONAL = "FUNCTIONAL"
    PERFORMANCE = "PERFORMANCE"
    SECURITY = "SECURITY"


class Verdict(str, Enum):
    """Validation verdicts."""
    PASS = "PASS"
    RETRY = "RETRY"
    ESCALATE = "ESCALATE"


class OWASPCategory(str, Enum):
    """OWASP Top 10 (2021) categories."""
    A01 = "A01"  # Broken Access Control
    A02 = "A02"  # Cryptographic Failures
    A03 = "A03"  # Injection
    A04 = "A04"  # Insecure Design
    A05 = "A05"  # Security Misconfiguration
    A06 = "A06"  # Vulnerable Components
    A07 = "A07"  # Auth Failures
    A08 = "A08"  # Data Integrity Failures
    A09 = "A09"  # Logging & Monitoring Failures
    A10 = "A10"  # SSRF


class SecuritySeverity(str, Enum):
    """OWASP finding severity levels."""
    CRITICAL = "CRITICAL"
    HIGH = "HIGH"
    MEDIUM = "MEDIUM"
    LOW = "LOW"


class SecurityVerdict(str, Enum):
    """OWASP security test verdicts."""
    VULNERABLE = "VULNERABLE"
    SECURE = "SECURE"
    INCONCLUSIVE = "INCONCLUSIVE"


class FunctionClassification(str, Enum):
    """Function type classification (determined by M2/M4)."""
    PURE_FUNCTION = "PURE_FUNCTION"
    UTILITY_HELPER = "UTILITY_HELPER"
    API_HANDLER = "API_HANDLER"
    SERVICE_LAYER = "SERVICE_LAYER"
    DOMAIN_LOGIC = "DOMAIN_LOGIC"
    DB_ACCESSOR = "DB_ACCESSOR"
    CACHE_LAYER = "CACHE_LAYER"
    WORKFLOW_ORCHESTRATOR = "WORKFLOW_ORCHESTRATOR"
    AUTH_HANDLER = "AUTH_HANDLER"
    DATA_PROCESSOR = "DATA_PROCESSOR"
    SERIALIZER = "SERIALIZER"


