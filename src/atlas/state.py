"""
ATLAS v3.0 — LangGraph State Schema

This module defines the ATLASState TypedDict used across all pipeline nodes.
Each field is documented per the ATLAS Master Agent Prompt specification.
"""

from __future__ import annotations

from enum import Enum
import operator
from typing import Annotated, Any, TypedDict

from langchain_core.messages import BaseMessage
from langgraph.graph.message import add_messages
from pydantic import BaseModel, Field


def merge_dicts(a: dict, b: dict) -> dict:
    """Reducer to merge two dictionaries."""
    return {**a, **b}


