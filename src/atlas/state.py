# Pydantic Models (for validated sub-structures)
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


class ProjectContext(BaseModel):
    """Project-level metadata detected by M1."""
    project_name: str
    language: str  # "python", "typescript", "javascript", "java", "go"
    framework: str | None = None  # "fastapi", "express", "spring", etc.
    test_framework: str  # "pytest", "jest", "vitest", "junit5"
    conventions: dict[str, Any] = Field(default_factory=dict)
    performance_budgets: dict[str, float] | None = None  # p95 budgets if configured


class ModuleContext(BaseModel):
    """Module-level metadata from M2 AST parser."""
    module_path: str
    imports: list[str] = Field(default_factory=list)
    exports: list[str] = Field(default_factory=list)
    dependencies: list[str] = Field(default_factory=list)
    dependency_tags: list[str] = Field(default_factory=list)  # ["neon", "db", "auth", ...]
    has_neon_imports: bool = False
    vulnerable_imports: list[dict[str, str]] = Field(default_factory=list)  # OWASP A06


class FunctionTarget(BaseModel):
    """Per-function analysis from M2/M4."""
    id: str  # Unique identifier: "module.function_name"
    name: str
    module_path: str
    source_code: str
    signature: str
    parameters: list[dict[str, Any]] = Field(default_factory=list)
    return_type: str | None = None
    decorators: list[str] = Field(default_factory=list)
    classification: FunctionClassification = FunctionClassification.PURE_FUNCTION
    cyclomatic_complexity: int = 1
    dependencies: list[str] = Field(default_factory=list)
    active_layers: list[TestLayer] = Field(default_factory=list)
    is_async: bool = False
    accepts_user_input: bool = False
    performs_auth: bool = False
    accesses_db: bool = False
    handles_files: bool = False
    calls_external: bool = False


class TestPlan(BaseModel):
    """Test plan generated by M4."""
    targets: list[FunctionTarget] = Field(default_factory=list)
    total_functions: int = 0
    total_layer_dispatches: int = 0  # sum of all active_layers across targets


class CoverageIntent(BaseModel):
    """Coverage metadata for a generated test suite."""
    layer_target: str
    branches_covered: list[str] = Field(default_factory=list)
    owasp_categories: list[str] = Field(default_factory=list)  # SECURITY layer only
    perf_budgets: dict[str, float] = Field(default_factory=dict)  # PERFORMANCE layer only
    known_gaps: list[str] = Field(default_factory=list)


class MockRequired(BaseModel):
    """A mock/fixture required by a test."""
    module: str
    mock_type: str  # "jest.mock", "unittest.mock", "pytest.fixture", etc.
    from_registry: bool = False
    fixture_key: str | None = None


class TestOutput(BaseModel):
    """Structured output from a layer agent (M5-*)."""
    target_file: str
    active_layer: TestLayer
    file_path: str
    framework: str
    confidence: float = Field(ge=0.0, le=1.0)
    reasoning: str
    history_used: str = "none"
    fixtures_reused: list[str] = Field(default_factory=list)
    fixtures_registered: list[str] = Field(default_factory=list)
    coverage_intent: CoverageIntent | None = None
    mocks_required: list[MockRequired] = Field(default_factory=list)
    test_code: str
    quality_flags: list[str] = Field(default_factory=list)


class SecurityFinding(BaseModel):
    """An OWASP security finding from M5-OWASP."""
    target_file: str
    function_name: str
    owasp_category: OWASPCategory
    severity: SecuritySeverity
    test_name: str
    test_code_snippet: str
    verdict: SecurityVerdict


class TestFileWrite(BaseModel):
    """Payload for writing the generated test to disk (M7)."""
    file_path: str
    content: str


class RejectionFeedback(BaseModel):
    """Feedback from M6 validator on a failed test."""
    layer: TestLayer
    target_file: str
    failures: list[dict[str, str]] = Field(default_factory=list)
    suggestions: list[str] = Field(default_factory=list)


