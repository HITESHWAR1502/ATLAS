# ATLASState — The LangGraph State TypedDict
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


class ATLASState(TypedDict, total=False):
    """
    The core state schema for the ATLAS LangGraph pipeline.

    This state flows through every node. Each node receives it and
    returns an updated version. LangGraph checkpoints the state after
    every node execution to maintain history.
    """

    # ── Identity ──────────────────────────────────────────────────────────────
    run_id: str                          # UUID for this pipeline run
    thread_id: str                       # LangGraph checkpoint thread ID
    target_file: str                     # File being tested
    attempt: int                         # Retry count per layer (starts at 1)
    active_layer: str                    # Current layer: UNIT|INTEGRATION|FUNCTIONAL

                                         #                PERFORMANCE|SECURITY

    # ── Upstream context (M1–M4) ──────────────────────────────────────────────
    project_context: dict[str, Any]      # Language, framework, conventions
    module_context: dict[str, Any]       # Module metadata, dependencies
    target_context: dict[str, Any]       # Source code, AST
    test_plan: dict[str, Any]            # M4-generated test plan + layer assignments


    # ── Git diff context (M0) ────────────────────────────────────────────────
    changed_files: list[str]             # Files changed in git diff
    diff_hunks: list[dict[str, Any]]     # Function-level diff details

    # ── Fan-out results (populated by parallel agents and merged) ─────────────
    layer_outputs: Annotated[dict[str, dict[str, Any]], merge_dicts]
    disk_writes_queue: Annotated[list[dict[str, Any]], operator.add]


    # ── Your output fields ────────────────────────────────────────────────────
    reasoning: str
    test_output: dict[str, Any]          # This layer's structured JSON output
    verdict: str                         # "PASS" | "RETRY" | "ESCALATE"

    # ── OWASP overlay (populated by M5-OWASP after JOIN) ─────────────────────
    owasp_output: dict[str, Any] | None
    security_findings: list[dict[str, Any]]

    # ── Feedback loop ─────────────────────────────────────────────────────────
    rejection_feedback: dict[str, Any] | None
    
    # ── Agentic Execution (new) ───────────────────────────────────────────────
    messages: Annotated[list[BaseMessage], add_messages]
    execution_result: dict[str, Any] | None
    retry_count: int
    max_retries: int
    selected_layers: list[str]
    current_layer_index: int
    execution_queue: list[dict[str, Any]]

    # ── Disk write payload (you populate; M7 executes) ────────────────────────
    disk_write: dict[str, Any]


    # ── Coverage results (M8) ────────────────────────────────────────────────
    coverage_results: dict[str, Any] | None

    # ── Error tracking ───────────────────────────────────────────────────────
    errors: list[str]
